const site_conf = {"www.gunnerkrigg.com": {"name": "Gunnerkrigg Court", "parenting": [0, 0], "next": ".extra .nav .right", "prev": ".extra .nav .left"}, "www.gpf-comics.com": {"name": "General Protection Fault", "parenting": [0, 0], "next": ".nav_link_forward a:first-child", "prev": ".nav_link_back a:last-child"}, "willsaveworldforgold.com": {"name": "Will Save World for Gold", "parenting": [0, 0], "next": ".navi-next", "prev": ".navi-prev"}, "mangareader.net": {"name": "Manga Reader.net", "parenting": [0, 0], "next": ".next", "prev": ".prev"}, "fanboys-online.com": {"name": "Fanboys Online", "parenting": [0, 0], "next": ".next", "prev": ".prev"}, "www.egscomics.com": {"name": "El Goonish Shrive", "parenting": [0, 0], "next": ".next", "prev": ".prev"}, "www.giantitp.com": {"name": "Order of the Stick", "parenting": [1, 1], "next": "a > img[alt=\"Next Comic\"]", "prev": "a > img[alt=\"Previous Comic\"]"}, "www.smbc-comics.com": {"name": "Saturday Morning Breakfast Cereal", "parenting": [0, 0], "next": "a.next", "prev": "a.prev"}};

// Generated by LiveScript 1.5.0
(function(){
  var KEY_CODE_LEFT, KEY_CODE_RIGHT, sitecfg;
  KEY_CODE_LEFT = 37;
  KEY_CODE_RIGHT = 39;
  sitecfg = site_conf[window.location.hostname];
  $(function(){
    var next, prev, i$, to$, _;
    next = document.querySelector(sitecfg.next);
    prev = document.querySelector(sitecfg.prev);
    for (i$ = 0, to$ = sitecfg.parenting[0]; i$ < to$; ++i$) {
      _ = i$;
      next = next.parentElement;
    }
    for (i$ = 0, to$ = sitecfg.parenting[1]; i$ < to$; ++i$) {
      _ = i$;
      prev = prev.parentElement;
    }
    $(window).keydown(function(e){
      if (prev != null && e.keyCode === KEY_CODE_LEFT) {
        e.preventDefault();
        location.href = prev.href;
      } else if (next != null && e.keyCode === KEY_CODE_RIGHT) {
        console.log("moving fwd");
        e.preventDefault();
        location.href = next.href;
      }
    });
    console.log("Webcomic Keyboard Shortcuts enabled; " + ("this site hosts " + sitecfg.name));
    if (next == null) {
      console.log("Will not be able to move forward on this page");
    }
    if (prev == null) {
      console.log("Will not be able to move backwards on this page");
    }
  });
}).call(this);
