const site_conf = {"www.smbc-comics.com": {"next": "a.next", "parenting": [0, 0], "name": "Saturday Morning Breakfast Cereal", "prev": "a.prev"}, "www.gpf-comics.com": {"next": ".nav_link_forward a:first-child", "parenting": [0, 0], "name": "General Protection Fault", "prev": ".nav_link_back a:last-child"}, "willsaveworldforgold.com": {"next": ".navi-next", "parenting": [0, 0], "name": "Will Save World for Gold", "prev": ".navi-prev"}, "www.giantitp.com": {"next": "a > img[alt=\"Next Comic\"]", "parenting": [1, 1], "name": "Order of the Stick", "prev": "a > img[alt=\"Previous Comic\"]"}, "fanboys-online.com": {"next": ".next", "parenting": [0, 0], "name": "Fanboys Online", "prev": ".prev"}, "mangareader.net": {"next": ".next", "parenting": [0, 0], "name": "Manga Reader.net", "prev": ".prev"}, "www.gunnerkrigg.com": {"next": ".extra .nav .right", "parenting": [0, 0], "name": "Gunnerkrigg Count", "prev": ".extra .nav .left"}, "www.egscomics.com": {"next": ".next", "parenting": [0, 0], "name": "El Goonish Shrive", "prev": ".prev"}};

